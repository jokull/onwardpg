import { execFileSync } from 'node:child_process';
import { mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import path from 'node:path';
import process from 'node:process';

const websiteRoot = process.cwd();
const repositoryRoot = path.resolve(websiteRoot, '..');
const destination = path.join(
  websiteRoot,
  'src/content/docs/reference/generated-cli-help.md',
);
const mode = process.argv[2] ?? '--check';
if (!['--check', '--write'].includes(mode)) {
  throw new Error('usage: node scripts/generate-cli-help.mjs [--check|--write]');
}

const commands = [
  { title: 'onwardpg', argv: ['--help'] },
  { title: 'onwardpg config check', argv: ['config', 'check', '--help'] },
  { title: 'onwardpg init', argv: ['init', '--help'] },
  { title: 'onwardpg plan', argv: ['plan', '--help'] },
  { title: 'onwardpg status', argv: ['status', '--help'] },
  { title: 'onwardpg verify', argv: ['verify', '--help'] },
  { title: 'onwardpg contract check', argv: ['contract', 'check', '--help'] },
  { title: 'onwardpg drift check', argv: ['drift', 'check', '--help'] },
  { title: 'onwardpg diff', argv: ['diff', '--help'] },
  { title: 'onwardpg history status', argv: ['history', 'status', '--help'] },
  { title: 'onwardpg dev plan', argv: ['dev', 'plan', '--help'] },
  { title: 'onwardpg draft', argv: ['draft', '--help'] },
  { title: 'onwardpg version', argv: ['version', '--help'] },
];

const fixture = await mkdtemp(path.join(tmpdir(), 'onwardpg-cli-docs-'));
const binary = path.join(fixture, 'onwardpg');

try {
  execFileSync('go', ['build', '-trimpath', '-o', binary, './cmd/onwardpg'], {
    cwd: repositoryRoot,
    stdio: ['ignore', 'pipe', 'inherit'],
  });

  const sections = commands.map(({ title, argv }) => {
    const help = execFileSync(binary, argv, {
      cwd: repositoryRoot,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'inherit'],
    }).replaceAll('\t', '    ').trimEnd();
    return `## \`${title}\`\n\n\`\`\`text\n${help}\n\`\`\`\n`;
  });

  const generated = `${[
    '---',
    'title: Generated CLI help',
    'description: Exact help emitted by the current onwardpg binary for every documented command surface.',
    '---',
    '',
    '<!-- Generated by website/scripts/generate-cli-help.mjs. Do not edit by hand. -->',
    '',
    'This page is generated from the current binary. For workflow, semantics, and examples, read the [CLI guide](/reference/cli).',
    '',
    ...sections,
  ].join('\n').trimEnd()}\n`;

  if (mode === '--write') {
    await writeFile(destination, generated);
    console.log(`wrote ${path.relative(websiteRoot, destination)}`);
  } else {
    const current = await readFile(destination, 'utf8').catch(() => '');
    if (current !== generated) {
      console.error('generated CLI help is stale; run: pnpm --dir website generate:cli-docs');
      process.exitCode = 1;
    } else {
      console.log(`CLI help is current: ${path.relative(websiteRoot, destination)}`);
    }
  }
} finally {
  await rm(fixture, { recursive: true, force: true });
}
